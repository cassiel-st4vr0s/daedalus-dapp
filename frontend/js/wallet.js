let provider;
let signer;
let contract;

/**
 * Conecta à carteira MetaMask, inicializa o Ethers e a instância do contrato.
 * @returns {Promise<boolean>} Retorna true se a conexão for bem-sucedida, false caso contrário.
 */
async function connectWallet() {
  if (typeof window.ethereum === "undefined") {
    console.error("MetaMask não está instalado!");
    alert("Por favor, instale a MetaMask para usar este DApp.");
    return false;
  }

  try {
    // Inicializa o provedor Ethers com o provedor da MetaMask
    provider = new ethers.BrowserProvider(window.ethereum);

    // Solicita ao usuário para conectar sua conta
    await provider.send("eth_requestAccounts", []);

    // Obtém o "signer", que é o objeto que pode assinar transações
    signer = await provider.getSigner();

    // Cria a instância do contrato, conectada ao signer
    contract = new ethers.Contract(contractAddress, contractABI, signer);

    console.log("Carteira conectada:", await signer.getAddress());
    console.log("Contrato inicializado:", contract);
    return true;
  } catch (error) {
    console.error("Falha ao conectar a carteira:", error);
    alert(`Erro ao conectar a carteira: ${error.message}`);
    return false;
  }
}

/**
 * Inicia a transação de compra de uma obra de arte.
 * @param {string} tokenId - O ID do token a ser comprado.
 * @param {string} priceInEth - O preço da obra em ETH (como string, ex: "1.5").
 * @returns {Promise<ethers.TransactionResponse>} A resposta da transação.
 */
async function purchaseArtwork(tokenId, priceInEth) {
  if (!contract || !signer) {
    alert("Por favor, conecte sua carteira primeiro.");
    const connected = await connectWallet();
    if (!connected) return; // Se a conexão falhar, aborta
  }

  try {
    // Converte o preço de ETH (string) para Wei (BigInt)
    const priceInWei = ethers.parseEther(priceInEth.toString());

    console.log(`Tentando comprar o token ${tokenId} por ${priceInWei} Wei...`);

    // Chama a função `buyArtwork` do smart contract
    const tx = await contract.buyArtwork(tokenId, {
      value: priceInWei,
    });

    console.log("Transação enviada:", tx.hash);
    return tx;
  } catch (error) {
    console.error("Erro na compra:", error);
    alert(`A transação falhou: ${error.reason || error.message}`);
    throw error;
  }
}
